"""Wave 3 batch 2 — snapshot-backed /summary (pure; DB + today_is_rest_day mocked).

whoop_summary() costs ~35s live (readiness + prevention + per-session Banister
loads). Wave 2.2 already stores the exact same JSON contract in
whoop_cockpit_snapshot.metrics_json, regenerated by the scheduler every few
hours. `_summary_from_snapshot` reads that instead when it's fresh and its
baked-in sabbath state agrees with today; otherwise both /summary call sites
(the Bearer route and the api-key /view route) fall back to the unchanged
live path via `_live_summary`.
"""

from __future__ import annotations

import asyncio
import json
import logging
from datetime import UTC, datetime, timedelta

from rivaflow.api.routes import whoop as whoop_route
from rivaflow.db.repositories.whoop_repo import WhoopRepository

FRESH_METRICS = {
    "readiness": {"state": "Prime", "score": 87, "headline": "green light to push"},
    "hrv_today": {"rmssd": 57},
    "resting_hr_today": {"resting_hr": 50},
    "sleep": {"available": True, "duration_hours": 7.6, "quality_score": 84},
}


def _snapshot_row(*, age: timedelta, metrics: dict | None = FRESH_METRICS) -> dict:
    rendered_at = datetime.now(UTC) - age
    return {
        "metrics_json": json.dumps(metrics) if metrics is not None else None,
        "deriver_version": "v3",
        "rendered_at": rendered_at,
    }


def _no_rest_day(_user_id: int) -> bool:
    return False


def _is_rest_day(_user_id: int) -> bool:
    return True


# ── _summary_from_snapshot ──────────────────────────────────────────────


def test_fresh_snapshot_served_with_source_and_as_of(monkeypatch):
    row = _snapshot_row(age=timedelta(minutes=5))
    monkeypatch.setattr(
        WhoopRepository, "get_cockpit_metrics", staticmethod(lambda uid: row)
    )
    monkeypatch.setattr(whoop_route, "today_is_rest_day", _no_rest_day)

    result = whoop_route._summary_from_snapshot(1)

    assert result is not None
    assert result["source"] == "snapshot"
    assert result["as_of"] == row["rendered_at"].isoformat()
    assert result["readiness"]["state"] == "Prime"


def test_stale_snapshot_falls_back_to_none(monkeypatch):
    row = _snapshot_row(age=timedelta(hours=4, minutes=1))
    monkeypatch.setattr(
        WhoopRepository, "get_cockpit_metrics", staticmethod(lambda uid: row)
    )
    monkeypatch.setattr(whoop_route, "today_is_rest_day", _no_rest_day)

    assert whoop_route._summary_from_snapshot(1) is None


def test_snapshot_exactly_at_ceiling_is_still_fresh(monkeypatch):
    # 4h ceiling is inclusive — only strictly older is stale.
    row = _snapshot_row(age=timedelta(hours=3, minutes=59))
    monkeypatch.setattr(
        WhoopRepository, "get_cockpit_metrics", staticmethod(lambda uid: row)
    )
    monkeypatch.setattr(whoop_route, "today_is_rest_day", _no_rest_day)

    assert whoop_route._summary_from_snapshot(1) is not None


