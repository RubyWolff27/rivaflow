# ── _live_summary ───────────────────────────────────────────────────────


def test_live_summary_carries_source_and_as_of(monkeypatch):
    monkeypatch.setattr(whoop_route, "today_is_rest_day", _no_rest_day)
    monkeypatch.setattr(
        whoop_route,
        "_timed_whoop_summary",
        lambda uid, today_is_sabbath: {"readiness": {"state": "Balanced"}},
    )

    result = whoop_route._live_summary(1)

    assert result["source"] == "live"
    assert "as_of" in result
    # Round-trips through fromisoformat without raising.
    datetime.fromisoformat(result["as_of"])
    assert result["readiness"]["state"] == "Balanced"


# ── route-level: /summary (Bearer call site) ────────────────────────────


def test_summary_route_uses_snapshot_when_fresh(monkeypatch):
    row = _snapshot_row(age=timedelta(minutes=5))
    monkeypatch.setattr(
        WhoopRepository, "get_cockpit_metrics", staticmethod(lambda uid: row)
    )
    monkeypatch.setattr(whoop_route, "today_is_rest_day", _no_rest_day)

    def _boom(uid, today_is_sabbath):
        raise AssertionError("live path should not run when snapshot is fresh")

    monkeypatch.setattr(whoop_route, "_timed_whoop_summary", _boom)

    # @route_error_handler always wraps the route in an async wrapper.
    result = asyncio.run(whoop_route.summary(current_user={"id": 1}))

    assert result["source"] == "snapshot"
    assert result["readiness"]["state"] == "Prime"


def test_summary_route_falls_back_to_live_when_snapshot_missing(monkeypatch):
    monkeypatch.setattr(
        WhoopRepository, "get_cockpit_metrics", staticmethod(lambda uid: None)
    )
    monkeypatch.setattr(whoop_route, "today_is_rest_day", _no_rest_day)
    monkeypatch.setattr(
        whoop_route,
        "_timed_whoop_summary",
        lambda uid, today_is_sabbath: {"readiness": {"state": "Building"}},
    )

    result = asyncio.run(whoop_route.summary(current_user={"id": 1}))

    assert result["source"] == "live"
    assert result["readiness"]["state"] == "Building"


# ── route-level: /view (api-key call site) shape ────────────────────────


def test_view_route_renders_html_from_snapshot(monkeypatch):
    row = _snapshot_row(age=timedelta(minutes=5))
    monkeypatch.setattr(
        WhoopRepository, "get_cockpit_metrics", staticmethod(lambda uid: row)
    )
    monkeypatch.setattr(whoop_route, "today_is_rest_day", _no_rest_day)
    monkeypatch.setattr(
        whoop_route, "_resolve_dashboard_key", lambda key: {"user_id": 1}
    )

    def _boom(uid, today_is_sabbath):
        raise AssertionError("live path should not run when snapshot is fresh")

    monkeypatch.setattr(whoop_route, "_timed_whoop_summary", _boom)

    response = whoop_route.view(key="whatever")

    assert response.status_code == 200
    assert b"Prime" in response.body
